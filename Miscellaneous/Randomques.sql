-- Q5. Find the highest total sales generated by a customer.
-----------------------------------------------------------
SELECT
    MAX(TotalSales) AS Highest_Total_Sales
FROM
(
    SELECT
        Customer_ID,
        SUM(Sales) AS TotalSales
    FROM Orders
    GROUP BY Customer_ID
) AS SalesTable;

-----------------------------------------------------------
-- Q6. Find the highest single order value
-- for each customer.
-----------------------------------------------------------
SELECT
    ID,
    First_Name,
    MAX(Sales) AS Highest_Sales
FROM Customers
LEFT JOIN Orders
    ON Customer_ID = ID
GROUP BY
    ID,
    First_Name
HAVING MAX(Sales) IS NOT NULL
ORDER BY Highest_Sales DESC;

