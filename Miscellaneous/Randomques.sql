-- Q4. Find customers who have never placed an order.
-----------------------------------------------------------
SELECT
    ID,
    First_Name,
    Country,
    Score
FROM Customers
WHERE ID NOT IN
(
    SELECT Customer_ID
    FROM Orders
);

