-- Q2. Find customers whose score is higher than
-- the average customer score.
-----------------------------------------------------------
SELECT
    ID,
    First_Name,
    Score
FROM Customers
WHERE Score >
(
    SELECT AVG(Score)
    FROM Customers
)
ORDER BY Score DESC;

